"""
   Enteros
     Los podemos sumar (+), restar (-),
     multiplicar (*) y dividir (/).
     Podemos aplicar potencias (**)
     y podemos aplicar el módulo (%).
"""

number_1 = 35
number_2 = 35
suma = number_1 + number_2
resta = number_2 - number_1
multiplicacion = number_1 * number_2
division = number_2 / number_1
potencia = number_1 ** 2
modulo = number_1 % number_2

print("Suma:", suma, type(suma))
print("Resta:", resta, type(resta))
print("Multiplicación:", multiplicacion, type(multiplicacion))
print("División:", division, type(division))
print("Módulo:", modulo, type(modulo))
print("Potencia:", potencia, type(potencia))

