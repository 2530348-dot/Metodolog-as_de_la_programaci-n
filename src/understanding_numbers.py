mod_1 = a % b

print("Suma:", sum_1, type(sum_1))
print("Resta:", res_1, type(res_1))
print("Multiplicación:", mul_1, type(mul_1))
print("División:", div_1, type(div_1))
print("Potencia:", pot_1, type(pot_1))
print("Módulo:", mod_1, type(mod_1))

